// Map ASCII to font index. Returns -1 for unsupported chars.
static inline int8_t fontIndex(char c) {
    if (c >= '0' && c <= '9') return c - '0';
    switch (c) {
        case ':': return 10;
        case ' ': return 11;
        case '-': return 12;
        case '.': return 13;
        case ',': return 14;
        case '%': return 15;
    }
    // Uppercase letters — crude but usable small caps
    // A-Z mapped to indices 16+
    return -1;
}

